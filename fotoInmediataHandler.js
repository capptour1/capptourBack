import pool from './src/db.js';

export function initFotoInmediata(io, usuariosConectados) {
    console.log('‚úÖ Foto Inmediata handler inicializado');

    io.on('connection', (socket) => {
        console.log('üü¢ Cliente conectado (foto inmediata):', socket.id);

        // Unirse a room de fot√≥grafo
        socket.on('join-fotografo', ({ fotografoId }) => {
            socket.join(`fotografo:${fotografoId}`);
            console.log(`üì∏ Fot√≥grafo ${fotografoId} unido al room fotografo:${fotografoId}`);

            // ‚úÖ DEBUG: Verificar clients en el room
            const room = io.sockets.adapter.rooms.get(`fotografo:${fotografoId}`);
            console.log(`üë• Clientes en room fotografo:${fotografoId}:`, room ? Array.from(room) : 'Vac√≠o');
        });

        // Unirse a room de usuario
        socket.on('join-usuario', ({ usuarioId }) => {
            socket.join(`usuario:${usuarioId}`);
            console.log(`üë§ Usuario ${usuarioId} unido al room usuario:${usuarioId}`);
        });

        // Solicitud de foto desde usuario
        socket.on('solicitar-foto', async ({ fotografoId, usuarioId, usuarioNombre }) => {
            try {
                console.log('üì® Evento solicitar-foto recibido:', { fotografoId, usuarioId, usuarioNombre });

                // ‚úÖ VALIDAR DATOS
                if (!fotografoId || fotografoId === 'null') {
                    console.log('‚ùå fotografoId inv√°lido');
                    return;
                }

                const fotografoIdNum = parseInt(fotografoId);
                const usuarioIdNum = parseInt(usuarioId);

                // ‚úÖ OBTENER fotografo_id REAL
                const fotografoResult = await pool.query(
                    'SELECT f.id as fotografo_id FROM fotografo.fotografos f WHERE f.usuario_id = $1',
                    [fotografoIdNum]
                );

                if (fotografoResult.rows.length === 0) {
                    console.log('‚ùå Fot√≥grafo no encontrado');
                    return;
                }

                const fotografoRealId = fotografoResult.rows[0].fotografo_id;

                // ‚úÖ INSERTAR SOLICITUD
                const result = await pool.query(
                    'INSERT INTO fotografo.solicitudes_foto (fotografo_id, usuario_id, estado) VALUES ($1, $2, $3) RETURNING *',
                    [fotografoRealId, usuarioIdNum, 'pendiente']
                );

                const solicitud = result.rows[0];
                console.log('‚úÖ Solicitud guardada ID:', solicitud.id);

                // ‚úÖ üî• SOLUCI√ìN DEFINITIVA: Buscar TODOS los sockets del fot√≥grafo
                const fotografoSockets = [];

                // Buscar en todos los sockets conectados
                const sockets = await io.fetchSockets();
                for (const clientSocket of sockets) {
                    // Si este socket pertenece al fot√≥grafo
                    if (clientSocket.rooms.has(`fotografo:${fotografoIdNum}`)) {
                        fotografoSockets.push(clientSocket.id);
                    }
                }

                console.log(`üîç Sockets del fot√≥grafo ${fotografoIdNum}:`, fotografoSockets);

                if (fotografoSockets.length > 0) {
                    // ‚úÖ ENVIAR A TODOS los sockets del fot√≥grafo
                    fotografoSockets.forEach(socketId => {
                        // ‚úÖ ENVIAR A M√öLTIPLES EVENTOS
                        const eventos = [
                            'nueva_solicitud_foto',      // guion bajo
                            'nueva-solicitud-foto',      // guion medio (original)
                            'nuevasolicitudfoto',        // sin guiones
                            'new_photo_request'          // ingl√©s
                        ];

                        eventos.forEach(evento => {
                            io.to(socketId).emit(evento, {
                                solicitudId: solicitud.id,
                                usuarioId: usuarioIdNum,
                                usuarioNombre: usuarioNombre,
                                fecha: new Date().toISOString()
                            });
                            console.log(`üì§ Enviado a evento: ${evento}`);
                        });
                    });
                    console.log(`üì§ Notificaci√≥n enviada a ${fotografoSockets.length} socket(s) del fot√≥grafo`);
                } else {
                    console.log('‚ö†Ô∏è Fot√≥grafo no tiene sockets activos');
                }

                // ‚úÖ CONFIRMAR AL USUARIO
                socket.emit('solicitud-enviada', {
                    solicitudId: solicitud.id,
                    mensaje: 'Solicitud enviada al fot√≥grafo'
                });

            } catch (error) {
                console.error('‚ùå Error en solicitar-foto:', error);
            }
        });

        // Fot√≥grafo acepta solicitud
        socket.on('aceptar-solicitud', async ({ solicitudId }) => {
            try {
                console.log(`‚úÖ Aceptando solicitud: ${solicitudId}`);

                await pool.query(
                    'UPDATE fotografo.solicitudes_foto SET estado = $1 WHERE id = $2',
                    ['aceptada', solicitudId]
                );

                const solicitudData = await pool.query(`
                    SELECT s.*, u.nombre_completo as usuario_nombre 
                    FROM fotografo.solicitudes_foto s 
                    JOIN auth.usuarios u ON s.usuario_id = u.id 
                    WHERE s.id = $1
                `, [solicitudId]);

                const solicitud = solicitudData.rows[0];

                // Notificar al usuario
                io.to(`usuario:${solicitud.usuario_id}`).emit('solicitud-aceptada', {
                    solicitudId: solicitudId,
                    mensaje: 'El fot√≥grafo ha aceptado tu solicitud'
                });

                // Notificar al fot√≥grafo que puede tomar la foto
                socket.emit('puede-tomar-foto', {
                    solicitudId: solicitudId,
                    usuarioId: solicitud.usuario_id,
                    usuarioNombre: solicitud.usuario_nombre
                });

                console.log('‚úÖ Solicitud aceptada y notificaciones enviadas');

            } catch (error) {
                console.error('‚ùå Error aceptando solicitud:', error);
                socket.emit('error-aceptar', {
                    message: 'Error al aceptar solicitud'
                });
            }
        });

        // Fot√≥grafo rechaza solicitud
        socket.on('rechazar-solicitud', async ({ solicitudId }) => {
            try {
                console.log(`‚ùå Rechazando solicitud: ${solicitudId}`);

                await pool.query(
                    'UPDATE fotografo.solicitudes_foto SET estado = $1 WHERE id = $2',
                    ['rechazada', solicitudId]
                );

                const solicitudData = await pool.query(
                    'SELECT usuario_id FROM fotografo.solicitudes_foto WHERE id = $1',
                    [solicitudId]
                );

                if (solicitudData.rows.length > 0) {
                    io.to(`usuario:${solicitudData.rows[0].usuario_id}`).emit('solicitud-rechazada', {
                        solicitudId: solicitudId,
                        mensaje: 'El fot√≥grafo ha rechazado tu solicitud'
                    });
                    console.log('‚úÖ Solicitud rechazada y usuario notificado');
                } else {
                    console.log('‚ùå No se encontr√≥ la solicitud para rechazar');
                }

            } catch (error) {
                console.error('‚ùå Error rechazando solicitud:', error);
            }
        });

        // Foto tomada por fot√≥grafo
        socket.on('foto-tomada', async ({ solicitudId, fotoUrl }) => {
            try {
                console.log(`üì∑ Foto tomada para solicitud: ${solicitudId}`);

                const solicitudData = await pool.query(`
                    SELECT s.*, u.nombre_completo as usuario_nombre 
                    FROM fotografo.solicitudes_foto s 
                    JOIN auth.usuarios u ON s.usuario_id = u.id 
                    WHERE s.id = $1
                `, [solicitudId]);

                if (solicitudData.rows.length === 0) {
                    console.log('‚ùå Solicitud no encontrada para foto');
                    return;
                }

                const solicitud = solicitudData.rows[0];

                // Guardar foto en BD
                const fotoResult = await pool.query(
                    `INSERT INTO fotografo.fotos_inmediatas 
                     (fotografo_id, usuario_id, foto_url) 
                     VALUES ($1, $2, $3) RETURNING *`,
                    [solicitud.fotografo_id, solicitud.usuario_id, fotoUrl]
                );

                // Marcar solicitud como completada
                await pool.query(
                    'UPDATE fotografo.solicitudes_foto SET estado = $1 WHERE id = $2',
                    ['completada', solicitudId]
                );

                // Notificar a AMBOS usuarios
                io.to(`usuario:${solicitud.usuario_id}`).emit('foto-guardada', {
                    solicitudId: solicitudId,
                    fotoUrl: fotoUrl,
                    mensaje: '¬°Tu foto ha sido guardada exitosamente!'
                });

                socket.emit('foto-procesada', {
                    solicitudId: solicitudId,
                    usuarioNombre: solicitud.usuario_nombre,
                    fotoUrl: fotoUrl,
                    mensaje: 'Foto guardada exitosamente'
                });

                console.log('‚úÖ Foto guardada y notificaciones enviadas');

            } catch (error) {
                console.error('‚ùå Error guardando foto:', error);
                socket.emit('error-foto', {
                    message: 'Error al guardar la foto'
                });
            }
        });

        // Manejar errores de socket
        socket.on('error', (error) => {
            console.error('‚ùå Error de socket:', error);
        });

        socket.on('disconnect', (reason) => {
            console.log('üî¥ Cliente desconectado (foto inmediata):', socket.id, 'Raz√≥n:', reason);
        });

        // Evento de ping para verificar conexi√≥n
        socket.on('ping', () => {
            socket.emit('pong', { timestamp: new Date().toISOString() });
        });
    });

    // Log peri√≥dico de rooms activos
    setInterval(() => {
        const rooms = io.sockets.adapter.rooms;
        console.log('üè† Rooms activos:', Array.from(rooms.keys()));
    }, 30000); // Cada 30 segundos
}